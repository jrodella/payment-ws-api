namespace Lyranetwork\Model;

class ExtInfo
{
    /**
     * @var string $key
     */
    private $key = null;

    /**
     * @var string $value
     */
    private $value = null;

    /**
     * @return string
     */
    public function getKey()
    {
        return $this->key;
    }

    /**
     * @param string $key
     * @return \Lyranetwork\ExtInfo
     */
    public function setKey($key)
    {
        $this->key = $key;
        return $this;
    }

    /**
     * @return string
     */
    public function getValue()
    {
        return $this->value;
    }

    /**
     * @param string $value
     * @return \Lyranetwork\ExtInfo
     */
    public function setValue($value)
    {
        $this->value = $value;
        return $this;
    }
}
